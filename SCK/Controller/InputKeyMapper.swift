func currentKeyDictionary(mode: KeyboardMode, shiftState: ShiftState) -> [String: String] {
    switch mode {
    case .default:
        return shiftState.isOn() ? defaultShiftKeys : defaultKeys
    case .subscriptMode:
        return shiftState.isOn() ? subScriptShiftKeys : subScriptKeys
    case .superscript:
        return shiftState.isOn() ? superScriptShiftKeys : superScriptKeys
    case .greek:
        return shiftState.isOn() ? greekShiftKeys : greekKeys
    case .math:
        return mathKeys
    }
}
func keyAction(for key: String, mode: KeyboardMode, shiftState: ShiftState) -> String? {
    let dictionary = currentKeyDictionary(mode: mode, shiftState: shiftState)
    
    return dictionary[key]
}
